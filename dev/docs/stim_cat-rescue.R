# cat rescue
cr1 <-
"1.<sup>1</sup>***The little girl*** <sup>2</sup>***was riding*** <sup>3</sup>***her bicycle***.

1. She (if appropriate referent), the girl, the child, any girl’s name
2. Rode, rides, was on, is playing on, stopped riding, got off, was beside, has
3. Bike, tricycle, trike, it (if appropriate referent)
"

cr2<-
"2.<sup>1</sup>***The cat*** <sup>2</sup>***was in*** <sup>3</sup>***the tree*** because the dog chased it.

1. Kitty, kitten, it (if appropriate referent), any cat name
2. Was up, was stuck in, got stuck in, climbed up, ran up, goes up, gets in, was caught in, ends up in, was on, was chased up, was scared up
3. The tree limb, limb

- Note: Sometimes expressed as “The dog <sup>w</sup>***chased*** <sup>1</sup>***the cat*** <sup>3</sup>***up the tree*.” or “The girl <sup>2</sup>***saw*** <sup>1</sup>***the cat*** <sup>3</sup>***in the tree*.”
"


cr3<-
"3.<sup>1</sup>***The dog*** <sup>2</sup>***was barking*** up the tree. 
1. It (if appropriate referent), puppy, pup, any dog name
2. Barks, is barking, barked, is yelping

- Note: “The dog chased the cat” should not apply to this statement as it was a separate relevant concept that did not meet threshold but was occasionally combined with additional elements that could apply to MC2 above.
"

cr4<-
"4.<sup>1</sup>***The man*** <sup>2</sup>***climbed up*** <sup>3</sup>***the tree.***
1. The neighbor, the father, dad, daddy, someone older, big brother, he (if appropriate referent), any man’s name
2. Was climbing, climbed, climbs, ran up, goes up into, got up on, crawls in/on
3. The branch, the limb, the ladder, it (if appropriate referent), there
"


cr5<-
"5.<sup>1</sup>***The man*** <sup>2</sup>***tries to rescue*** <sup>3</sup>***the cat***.
1. See 4.1
2. Wants to help, wants to rescue, tries to get, attempts to get, tries to reach, goes to get, tries to retrieve, went up after, comes to rescue
3. See 2.1

- Note: Frequently combined with MC 4 as in “The man climbed up the tree to get the cat.”; a person who says this should receive full credit for MCs 4 and 5.
- Note: Occasionally combined with MC 7 as in “He’s stuck in the tree trying to get the cat.”; a person who says this should receive full credit for MCs 5 and 7.
"


cr6<-
"6.<sup>1</sup>***The ladder*** <sup>2</sup>***fell down***.
1. It (if appropriate referent)
2. Is down, falls, fell, has fallen, has fallen down, got away from him, is on the ground, has slipped away, has dropped away, fell off, has been knocked down

- Note: Sometimes expressed with an agent that caused the ladder to fall, such as the wind or dog (e.g., “the dog knocked the ladder down”).
"



cr7<-
"7.<sup>1</sup>***The father*** <sup>2</sup>***is stuck*** <sup>3</sup>***in the tree*** with the cat.
1. See 4.1, the man and the cat, they (if appropriate referents)
2. Is up, is, is stranded, is caught, ended up, is marooned, is sitting
3. On the branch, on the limb, up there

- Note: Sometimes expressed as: “<sup>1</sup>***The man*** <sup>2</sup>***couldn’t*** <sup>3</sup>***get down.*”
"


cr8<-
"8.<sup>1</sup>***Someone*** <sup>2</sup>***called*** <sup>3</sup>***the fire department***.
1. The mother, the neighbor, the lady next door, the girl, the father, a passerby, an onlooker, he/she/they
2. Notifies, alerts, got
3. The firemen, 911

- Note: Sometimes expressed as a passive such as: “<sup>3</sup>***The fire*** department <sup>2</sup>***has been called.*”
- Note: For this concept, a pronoun without a preceding referent is scored as AC since this action is not depicted in the picture stimuli.
"

cr9<-
"9.<sup>1</sup>***The fire department*** <sup>2</sup>***comes*** <sup>3</sup>***with a ladder***.
1. The firefighters, the firemen, the fire truck, they (if appropriate referent or if includes ladder or other context so that the referent is not ambiguous)
2. Is on the way, is/are coming, came, have arrived, rushes out, brings

- Note: Sometimes combined with MC 8 as in “The mother called the fire department to come with their ladder.” A person who says this should receive full credit for MCs 8 and 9.
- Note: While the first two essential elements met 66% threshold, the final element “with a ladder” was only produced by 33% of the sample. 
"


cr10<-
"10.<sup>1</sup>***The fire department*** <sup>2</sup>***rescues*** <sup>3</sup>***them***.
1. See 9.1 (but not fire truck)
2. Saves, is going to get, helps, gets, will take 
3. The man, the cat, the man and the cat

- Note: Often combined with MC 9 as in “The fire department comes with a ladder to rescue them.” A person who says this should receive full credit for MCs 9 and 10.
- Note: Sometimes combined with MC 8 and MC 9 as in “The mother called the fire department to come and rescue the father with a ladder.” A person who says this should receive full credit for MCs 8, 9, and 10.
